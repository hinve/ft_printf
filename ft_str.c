#include "ft_printf.h"

int	ft_str(char *str)
{
	int	i;

	if (!str)
	{
		write(1, "(null)", 6);
		i = 6;
		return (i);
	}
	i = 0;
	while (str[i])
	{
		ft_chr(str[i]);
		i++;
	}
	return (i);
}
