#include "ft_printf.h"

int	ft_p(unsigned long p)
{
	int	i;

	i = 0;
	if (!p)
	{
		ft_str("0x0");
		return (3);
	}
	else
	{
		ft_str("0x");
		i += ft_xnbr(p);
		return (i + 2);
	}
}
